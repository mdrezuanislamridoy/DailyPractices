package String_;

public class toStrings {
    public static void main(String[] args) {
        String s= new String("Do It");
        System.out.println(s);

        Integer a= 9;
        String v= a.toString();
        System.out.println(v); 

        Long b= 999L;
        String g= b.toString();
        System.out.println(g); 

        Character c= 'g';
        String n= c.toString();
        System.out.println(n); 

        Float j= 9.9f;
        String k= j.toString();
        System.out.println(k); 
    }
}
